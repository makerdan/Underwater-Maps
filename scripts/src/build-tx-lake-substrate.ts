function ringToLonLat(local: XY[], originLat: number): number[][] {
  const out = local.map((p) => toLonLat(p, originLat));
  if (out.length === 0) return [];
  const first = out[0]!;
  out.push([first[0], first[1]]);
  return out;
}

/**
 * Douglas–Peucker polyline simplification in local-metres coordinates.
 * Drops vertices whose perpendicular distance to the simplified line is
 * less than `toleranceMeters`. Critical for NHD waterbody polygons which
 * carry tens of thousands of vertices per shoreline — without
 * simplification the bundled JSON would be 40+ MB per lake.
 */
function simplifyLocal(points: XY[], toleranceMeters: number): XY[] {
  if (points.length < 3) return [...points];
  const sqTol = toleranceMeters * toleranceMeters;
  const keep = new Uint8Array(points.length);
  keep[0] = 1;
  keep[points.length - 1] = 1;
  const stack: [number, number][] = [[0, points.length - 1]];
  const sqDistSeg = (p: XY, a: XY, b: XY): number => {
    let x = a.x, y = a.y;
    let dx = b.x - x, dy = b.y - y;
    if (dx !== 0 || dy !== 0) {
      const t = ((p.x - x) * dx + (p.y - y) * dy) / (dx * dx + dy * dy);
      if (t > 1) { x = b.x; y = b.y; }
      else if (t > 0) { x += dx * t; y += dy * t; }
    }
    dx = p.x - x; dy = p.y - y;
    return dx * dx + dy * dy;
  };
  while (stack.length > 0) {
    const [lo, hi] = stack.pop()!;
    let maxSq = 0, maxIdx = -1;
    for (let i = lo + 1; i < hi; i++) {
      const sq = sqDistSeg(points[i]!, points[lo]!, points[hi]!);
      if (sq > maxSq) { maxSq = sq; maxIdx = i; }
    }
    if (maxSq > sqTol && maxIdx >= 0) {
      keep[maxIdx] = 1;
      stack.push([lo, maxIdx], [maxIdx, hi]);
    }
  }
  const out: XY[] = [];
  for (let i = 0; i < points.length; i++) if (keep[i]) out.push(points[i]!);
  return out;
}

/** Simplification tolerance for shoreline rings, in metres. ~30 m matches
 *  the natural granularity of the resolved 32×32 classification grid for
 *  the Texas-reservoir AOIs and shrinks bundled JSON by ~99 %. */
const SIMPLIFY_TOLERANCE_M = 30;

function makeProps(
  spec: LakeSpec,
  substrate: CmecsClass,
  label: string,
  suffix: string,
  natsur: string,
): BundledProps {
  return {
    unitId: `${spec.datasetId}-${suffix}`,
    substrate,
    shoreZoneClass: label,
    cmecsCode: CMECS[substrate],
    color: COLOR[substrate],
    source: "tpwd-tx-reservoirs",
    natsur,
    natqua: null,
    encChart: spec.lakePageUrl,
  };
}

function buildLakeFeatures(
  spec: LakeSpec,
  esri: EsriPolygon,
  tpwdPts: TpwdPoint[],
): BundledFeature[] {
  const originLat = (spec.bbox[1] + spec.bbox[3]) / 2;
  const outerLocalRings: XY[][] = [];
  for (const ring of esri.rings) {
    const local = ring.map(([lo, la]) => toLocal(lo!, la!, originLat));
    if (local.length < 4) continue;
    const simplified = simplifyLocal(local, SIMPLIFY_TOLERANCE_M);
    if (simplified.length >= 4) outerLocalRings.push(simplified);
  }
  if (outerLocalRings.length === 0) return [];

  const mainBasinPolys: number[][][][] = [];
  const littoralPolys: number[][][][] = [];
  for (const outer of outerLocalRings) {
    const eroded = erodeRing(outer, spec.litoralBeltMeters);
    const outerLonLat = ringToLonLat(outer, originLat);
    if (eroded.length === 0) {
      littoralPolys.push([outerLonLat]);
      continue;
    }
    const erodedLonLat = ringToLonLat(eroded, originLat);
    mainBasinPolys.push([erodedLonLat]);
    littoralPolys.push([outerLonLat, erodedLonLat.slice().reverse()]);
  }

  const out: BundledFeature[] = [];

  // Mud basin
  if (mainBasinPolys.length === 1) {
    out.push({
      type: "Feature",
      properties: makeProps(spec, "mud", "Main basin / inundated channels (mud/clay)", "basin", spec.bottomNarrative),
      geometry: { type: "Polygon", coordinates: mainBasinPolys[0]! },
    });
  } else if (mainBasinPolys.length > 1) {
    out.push({
      type: "Feature",
      properties: makeProps(spec, "mud", "Main basin / inundated channels (mud/clay)", "basin", spec.bottomNarrative),
      geometry: { type: "MultiPolygon", coordinates: mainBasinPolys },
    });
  }
  // Sand littoral belt
  if (littoralPolys.length === 1) {
    out.push({
      type: "Feature",
      properties: makeProps(spec, "sand", "Littoral shoreline belt (sand/loam)", "littoral", spec.bottomNarrative),
      geometry: { type: "Polygon", coordinates: littoralPolys[0]! },
    });
  } else if (littoralPolys.length > 1) {
    out.push({
      type: "Feature",
      properties: makeProps(spec, "sand", "Littoral shoreline belt (sand/loam)", "littoral", spec.bottomNarrative),
      geometry: { type: "MultiPolygon", coordinates: littoralPolys },
    });
  }

  // Curated structure zones (small squares around documented features).
  for (const zone of spec.curatedZones) {
    const c = toLocal(zone.center[0], zone.center[1], originLat);
    const h = zone.halfWidthM;
    const local: XY[] = [
      { x: c.x - h, y: c.y - h },
      { x: c.x + h, y: c.y - h },
      { x: c.x + h, y: c.y + h },
      { x: c.x - h, y: c.y + h },
    ];
    out.push({
      type: "Feature",
      properties: makeProps(spec, zone.substrate, zone.label, `zone-${zone.id}`, zone.citation),
      geometry: { type: "Polygon", coordinates: [ringToLonLat(local, originLat)] },
    });
  }

  // TPWD habitat-structure clusters → gravel zones (one per cluster).
  if (tpwdPts.length > 0) {
    const localPts = tpwdPts.map((p) => toLocal(p.lon, p.lat, originLat));
    const groups = clusterByDistance(localPts, spec.habitatClusterMeters);
    let emitted = 0;
    for (const idxs of groups) {
      if (idxs.length < spec.habitatMinClusterPoints) continue;
      const groupXY = idxs.map((i) => localPts[i]!);
      const hull = convexHull(groupXY);
      const buffered = bufferConvexRing(hull, Math.max(60, spec.habitatClusterMeters * 0.4), 8);
      if (buffered.length < 4) continue;
      const detail = tpwdPts[idxs[0]!]!.typeDetail || tpwdPts[idxs[0]!]!.typeGeneral || "structure";
      emitted += 1;
      out.push({
        type: "Feature",
        properties: makeProps(
          spec,
          "gravel",
          `TPWD habitat-structure cluster (${detail}, n=${idxs.length})`,
          `tpwd-cluster-${emitted}`,
          `TPWD-deployed fish habitat structures (${detail}); ${idxs.length} sites grouped within ${spec.habitatClusterMeters} m.`,
        ),
        geometry: { type: "Polygon", coordinates: [ringToLonLat(buffered, originLat)] },
      });
    }
    console.log(`    TPWD clusters: ${groups.length} groups, emitted ${emitted}`);
  }

  return out;
}

