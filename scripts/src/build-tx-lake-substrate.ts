/**
 * build-tx-lake-substrate.ts — Build a real-geometry freshwater substrate
 * bundle for the Texas reservoir presets (Lake Fork, Sam Rayburn,
 * Lake Ray Roberts, Toledo Bend).
 *
 * Why this exists: the existing SE Alaska substrate bundles (Alaska
 * ShoreZone + NOAA ENC) don't cover freshwater Texas reservoirs, so the
 * zone classifier falls back to depth-only inference for those datasets.
 * This script ingests real lake-outline polygons from the USGS National
 * Hydrography Dataset (NHD Waterbody, FTYPE=390) plus surveyed
 * structure-zone data from TPWD Inland Fisheries and TWDB volumetric
 * reservoir surveys, producing multiple substrate classes per lake
 * instead of a uniform mud basin:
 *
 *   • Main basin / inundated channels — mud
 *     (TWDB volumetric surveys document fine-sediment accumulation in
 *      the inundated river / creek channels and the deep basin.)
 *   • Shoreline littoral belt — sand
 *     (TPWD lake-survey reports describe the lakes as having
 *      sand-to-loam littoral shorelines with submerged timber.)
 *   • Surveyed structure zones — gravel
 *     Two real, per-lake sources:
 *       a) Curated polygons for documented gravel humps, dam riprap,
 *          submerged-creek-channel gravel bars and standing-timber
 *          flats called out in the TPWD lake-survey reports.
 *       b) Spatial clusters of TPWD-deployed Texas Fish Habitat
 *          Structures (brushpiles, PVC cubes, artificial reef cubes)
 *          fetched live from the TPWD FeatureServer. These artificial-
 *          habitat clusters are classified as "mixed coarse" structure
 *          (gravel CMECS bucket) because TPWD deploys them on hard,
 *          structure-rich bottoms relative to the surrounding mud basin.
 *
 * The shoreline belt is generated by inward-buffering the NHD waterbody
 * outline by a per-lake distance (~150 m); the inner remainder is the
 * main-basin polygon. Structure zones are added as additional features
 * on top of those two layers.
 *
 * Lake Fork NHD selection: the NHD Waterbody layer mis-labels the main
 * reservoir polygon (the largest-by-area match in bbox is actually a
 * small adjacent waterbody, "Case Lake", not Lake Fork Reservoir). We
 * resolve the correct polygon by combining (1) GNIS_NAME alias lookup
 * with (2) a per-lake centroid + minimum-area guard that rejects
 * mis-named small ponds in favour of the true reservoir outline.
 *
 * Sources:
 *   - USGS National Hydrography Dataset — Waterbody (large scale, layer 12)
 *     https://hydro.nationalmap.gov/arcgis/rest/services/nhd/MapServer/12
 *   - TPWD Texas Fish Habitat Structures FeatureServer
 *     https://services1.arcgis.com/1mtXwieMId59thmg/arcgis/rest/services/
 *       Texas_Fish_Habitat_Structures_20221206/FeatureServer/1
 *   - TPWD Inland Fisheries lake-survey reports (lake-bottom narratives):
 *     https://tpwd.texas.gov/fishboat/fish/recreational/lakes/fork/
 *     https://tpwd.texas.gov/fishboat/fish/recreational/lakes/samrayburn/
 *     https://tpwd.texas.gov/fishboat/fish/recreational/lakes/rayroberts/
 *     https://tpwd.texas.gov/fishboat/fish/recreational/lakes/toledobend/
 *   - TWDB Reservoir Volumetric & Sedimentation Surveys:
 *     https://www.twdb.texas.gov/surfacewater/surveys/index.asp
 *
 * Usage:
 *   pnpm --filter @workspace/scripts run build-tx-lake-substrate
 */

