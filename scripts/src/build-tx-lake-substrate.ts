const METERS_PER_DEG_LAT = 111_320;
function metersPerDegLon(latDeg: number): number {
  return METERS_PER_DEG_LAT * Math.cos((latDeg * Math.PI) / 180);
}

interface XY { x: number; y: number }
function toLocal(lon: number, lat: number, originLat: number): XY {
  return { x: lon * metersPerDegLon(originLat), y: lat * METERS_PER_DEG_LAT };
}
function toLonLat(xy: XY, originLat: number): [number, number] {
  return [xy.x / metersPerDegLon(originLat), xy.y / METERS_PER_DEG_LAT];
}

/** Signed area in local metres (CCW positive). */
function ringArea(ring: XY[]): number {
  let a = 0;
  for (let i = 0, n = ring.length; i < n; i++) {
    const p = ring[i]!;
    const q = ring[(i + 1) % n]!;
    a += p.x * q.y - q.x * p.y;
  }
  return a / 2;
}

/**
 * Approximate inward buffer (erosion) of a simple polygon ring by `r`
 * metres. For each vertex we shift inward along the average of the two
 * incident-edge inward normals, scaled by 1/cos(half-angle) so straight
 * stretches end up exactly `r` away from the original edge. The result
 * is a coarser ring but is good enough to produce a recognisable
 * shoreline-belt vs main-basin split for visualisation and grounding.
 *
 * If the ring is too small to be eroded by `r`, returns an empty array.
 */
function erodeRing(ring: XY[], r: number): XY[] {
  if (ring.length < 4) return [];
  // Ensure CCW so the inward normal is consistent.
  const closed = ring[0]!.x === ring[ring.length - 1]!.x && ring[0]!.y === ring[ring.length - 1]!.y
    ? ring.slice(0, -1)
    : ring.slice();
  const ccw = ringArea(closed) > 0;
  const ordered = ccw ? closed : closed.slice().reverse();
  const n = ordered.length;
  const out: XY[] = [];
  for (let i = 0; i < n; i++) {
    const prev = ordered[(i - 1 + n) % n]!;
    const curr = ordered[i]!;
    const next = ordered[(i + 1) % n]!;
    const inAng = Math.atan2(curr.y - prev.y, curr.x - prev.x);
    const outAng = Math.atan2(next.y - curr.y, next.x - curr.x);
    // For CCW ring the inward normal of an edge with direction θ is θ - π/2.
    const nIn = inAng - Math.PI / 2;
    const nOut = outAng - Math.PI / 2;
    let diff = nOut - nIn;
    while (diff > Math.PI) diff -= 2 * Math.PI;
    while (diff < -Math.PI) diff += 2 * Math.PI;
    const mid = nIn + diff / 2;
    const scale = r / Math.max(0.25, Math.cos(diff / 2));
    out.push({ x: curr.x + scale * Math.cos(mid), y: curr.y + scale * Math.sin(mid) });
  }
  // Reject degenerate eroded rings.
  if (out.length < 4 || Math.abs(ringArea(out)) < r * r * 4) return [];
  return out;
}

/** Andrew's monotone-chain convex hull (2D). */
function convexHull(pts: XY[]): XY[] {
  if (pts.length < 3) return [...pts];
  const sorted = [...pts].sort((a, b) => a.x - b.x || a.y - b.y);
  const cross = (o: XY, a: XY, b: XY): number =>
    (a.x - o.x) * (b.y - o.y) - (a.y - o.y) * (b.x - o.x);
  const lower: XY[] = [];
  for (const p of sorted) {
    while (lower.length >= 2 && cross(lower[lower.length - 2]!, lower[lower.length - 1]!, p) <= 0) lower.pop();
    lower.push(p);
  }
  const upper: XY[] = [];
  for (let i = sorted.length - 1; i >= 0; i--) {
    const p = sorted[i]!;
    while (upper.length >= 2 && cross(upper[upper.length - 2]!, upper[upper.length - 1]!, p) <= 0) upper.pop();
    upper.push(p);
  }
  upper.pop(); lower.pop();
  return lower.concat(upper);
}

/** Outward-buffer a (possibly tiny) polygon by `r` metres — approximated
 *  by adding a circular arc at each vertex of the convex hull. Used to
 *  give TPWD habitat-structure clusters a visible footprint. */
function bufferConvexRing(ring: XY[], r: number, arcSegments = 8): XY[] {
  if (ring.length === 0) return [];
  if (ring.length === 1) {
    const out: XY[] = [];
    const p = ring[0]!;
    const steps = arcSegments * 4;
    for (let k = 0; k < steps; k++) {
      const t = (k / steps) * 2 * Math.PI;
      out.push({ x: p.x + r * Math.cos(t), y: p.y + r * Math.sin(t) });
    }
    return out;
  }
  const n = ring.length;
  const out: XY[] = [];
  for (let i = 0; i < n; i++) {
    const prev = ring[(i - 1 + n) % n]!;
    const curr = ring[i]!;
    const next = ring[(i + 1) % n]!;
    const inA = Math.atan2(curr.y - prev.y, curr.x - prev.x);
    const outA = Math.atan2(next.y - curr.y, next.x - curr.x);
    const nIn = inA + Math.PI / 2;
    const nOut = outA + Math.PI / 2;
    let delta = nOut - nIn;
    while (delta > Math.PI) delta -= 2 * Math.PI;
    while (delta < -Math.PI) delta += 2 * Math.PI;
    if (delta >= 0) {
      for (let k = 0; k <= arcSegments; k++) {
        const t = nIn + (delta * k) / arcSegments;
        out.push({ x: curr.x + r * Math.cos(t), y: curr.y + r * Math.sin(t) });
      }
    } else {
      const mid = nIn + delta / 2;
      const mlen = r / Math.max(0.2, Math.cos(delta / 2));
      out.push({ x: curr.x + mlen * Math.cos(mid), y: curr.y + mlen * Math.sin(mid) });
    }
  }
  return out;
}

/** Single-link cluster TPWD habitat-structure points by max-link distance. */
function clusterByDistance(pts: XY[], maxMeters: number): number[][] {
  const n = pts.length;
  const parent = Array.from({ length: n }, (_, i) => i);
  const find = (i: number): number => {
    while (parent[i]! !== i) { parent[i] = parent[parent[i]!]!; i = parent[i]!; }
    return i;
  };
  const union = (a: number, b: number): void => {
    const ra = find(a), rb = find(b);
    if (ra !== rb) parent[ra] = rb;
  };
  const r2 = maxMeters * maxMeters;
  for (let i = 0; i < n; i++) {
    for (let j = i + 1; j < n; j++) {
      const dx = pts[i]!.x - pts[j]!.x;
      const dy = pts[i]!.y - pts[j]!.y;
      if (dx * dx + dy * dy <= r2) union(i, j);
    }
  }
  const groups = new Map<number, number[]>();
  for (let i = 0; i < n; i++) {
    const r = find(i);
    let g = groups.get(r);
    if (!g) { g = []; groups.set(r, g); }
    g.push(i);
  }
  return [...groups.values()];
}

